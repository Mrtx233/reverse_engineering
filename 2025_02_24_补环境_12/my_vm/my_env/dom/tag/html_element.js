my_env.plugins.def_obj_prop(HTMLElement.prototype, "onclick", true, true, undefined, undefined,
    function onclick() {
        if (this instanceof HTMLElement) {
            // 获取到onclick属性值
            let obj_listeners = my_env.memory.listeners.get(this);
            if(!obj_listeners){
                return null;
            }
            let callbacks = obj_listeners['click'];
            if(!callbacks){
                return null;
            }
            for(let i = 0 ; i < callbacks.length; i++){
                let callback = callbacks[i];
                if(callback["element.onclick"] === 1){
                    return callback;
                }
            }
            return null;
        } else {
            my_env.plugins.throw_error(TypeError, "Illegal invocation");
        }
    },
    // 设置onclick
    function onclick(func) {
        // 先获取到之前存储的func. 从回调列表中删除它, 然后再加新的
        if (this instanceof HTMLElement) {
            let old_func = this.onclick;
            if(old_func){
                delete old_func["element.onclick"];
                // 删掉之前的
                this.removeEventListener("click", old_func);
            }
            // 回调函数必须要有
            if (func){
                func["element.onclick"] = 1  // 增加一个标志.用来标记该函数是属于on系列的属性
                // 直接加到回调列表中
                this.addEventListener("click", func);
            }
        } else {
            my_env.plugins.throw_error(TypeError, "Illegal invocation");
        }
    }
);

